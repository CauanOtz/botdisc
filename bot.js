// Mantendo o resto do c√≥digo de participa√ß√£o e cancelamento igual
client.on('interactionCreate', async (interaction) => {
    if(!interaction.isButton()) return;

    const [action, actionId] = interaction.customId.split('_');
    const actionData = actions[actionId];

    if(!actionData) return interaction.reply({ content: 'A√ß√£o n√£o encontrada.', ephemeral: true });

    if(action === 'Participar'){
        if(actionData.participantes.includes(interaction.user.id)){
            actionData.participantes = actionData.participantes.filter(id => id !== interaction.user.id);
            actionData.reservas = actionData.reservas.filter(id => id !== interaction.user.id);
            await interaction.reply({content: 'Voc√™ foi removido da lista de participantes.', ephemeral: true});
        }else if(actionData.participantes.length < actionData.vagas){
            actionData.participantes.push(interaction.user.id);
            await interaction.reply({content: 'Voc√™ foi adicionado a lista de participantes.', ephemeral: true});
        }else {
            actionData.reservas.push(interaction.user.id);
            await interaction.reply({content: 'Voc√™ foi adicionado a lista de reservas.', ephemeral: true});
        }

        // Formatando a lista de participantes com n√∫meros
        const participantesList = actionData.participantes.length > 0 
            ? actionData.participantes.map((id, index) => `${index + 1}. <@${id}>`).join('\n')
            : '*Nenhum participante ainda*';

        // Formatando a lista de reservas com n√∫meros
        const reservasList = actionData.reservas.length > 0
            ? actionData.reservas.map((id, index) => `${index + 1}. <@${id}>`).join('\n')
            : '*Nenhuma reserva ainda*';

        const buttons = new ActionRowBuilder()
            .addComponents(
                new ButtonBuilder()
                    .setCustomId(`Participar_${actionId}`)
                    .setLabel(actionData.participantes.includes(interaction.user.id) ? '‚ùå Se Retirar' : '‚úÖ Participar')
                    .setStyle(actionData.participantes.includes(interaction.user.id) ? ButtonStyle.Danger : ButtonStyle.Success),
                new ButtonBuilder()
                    .setCustomId(`Cancelar_${actionId}`)
                    .setLabel('‚ùå Cancelar')
                    .setStyle(ButtonStyle.Danger)
            );

        await interaction.message.edit({
            embeds: [{
                color: 0x0099FF,
                title: `üéÆ ${actionData.name}`,
                description: `
üìÖ **Data:** <t:${Math.floor(actionId / 1000)}:F>

üë• **Vagas:** ${actionData.participantes.length}/${actionData.vagas}
üó°Ô∏è **Arma do ba√∫:** ${actionData.pegouArma ? 'Sim' : 'N√£o'}

**Participantes:**
${participantesList}

${actionData.reservas.length > 0 ? `**Reservas:**\n${reservasList}` : ''}`,
                footer: {
                    text: 'Use os bot√µes abaixo para participar ou se retirar da a√ß√£o!'
                }
            }],
            components: [buttons]
        });
    }

    if(action === 'Cancelar'){
        const select = new ActionRowBuilder()
            .addComponents(
                new SelectMenuBuilder()
                    .setCustomId(`status_${actionId}`)
                    .setPlaceholder('Selecione o status da a√ß√£o')
                    .addOptions([
                        { label: 'üèÜ Vit√≥ria', value: 'vitoria' },
                        { label: 'üíÄ Derrota', value: 'derrota' }
                    ])
            );

        await interaction.reply({ content: '‚öîÔ∏è Qual foi o status da a√ß√£o?', components: [select], ephemeral: true });
    }
});

client.on('interactionCreate', async (interaction) => {
    if(!interaction.isSelectMenu()) return;

    const [_, actionId] = interaction.customId.split('_');
    const actionData = actions[actionId];

    if(!actionData) return interaction.reply({ content: 'A√ß√£o n√£o encontrada.', ephemeral: true });

    const status = interaction.values[0];
    const participantes = actionData.participantes.map(id => `<@${id}>`).join('\n') || 'Nenhum participante';

    await interaction.channel.send({
        embeds: [{
            color: status === 'vitoria' ? 0x00FF00 : 0xFF0000,
            title: 'üéÆ Resultado da A√ß√£o',
            fields: [
                { name: 'üé≠ A√ß√£o', value: actionData.name, inline: true },
                { name: 'üìÖ Data', value: `<t:${Math.floor(actionId / 1000)}:F>`, inline: true },
                { name: '‚öîÔ∏è Status', value: status === 'vitoria' ? 'üèÜ Vit√≥ria' : 'üíÄ Derrota', inline: true },
                { name: 'üë• Participantes', value: participantes }
            ]
        }]
    });

    delete actions[actionId];
    await interaction.reply({ content: 'A√ß√£o finalizada com sucesso!', ephemeral: true });
});

