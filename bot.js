client.on('interactionCreate', async (interaction) => {
    if (interaction.isButton() && interaction.customId === 'criar_acao') {
        const modal = new ModalBuilder()
            .setCustomId('modal_acao')
            .setTitle('Criar Nova A√ß√£o');

        const nomeInput = new TextInputBuilder()
            .setCustomId('nome_acao')
            .setLabel('Nome da A√ß√£o')
            .setStyle(TextInputStyle.Short)
            .setPlaceholder('Digite o nome da a√ß√£o')
            .setRequired(true);

        const vagasInput = new TextInputBuilder()
            .setCustomId('vagas_acao')
            .setLabel('N√∫mero de Vagas')
            .setStyle(TextInputStyle.Short)
            .setPlaceholder('Digite o n√∫mero de vagas')
            .setRequired(true);

        // Primeiro mostramos apenas o modal b√°sico
        modal.addComponents(
            new ActionRowBuilder().addComponents(nomeInput),
            new ActionRowBuilder().addComponents(vagasInput)
        );

        await interaction.showModal(modal);
    }
});

// Tratando a submiss√£o do modal inicial
client.on('interactionCreate', async (interaction) => {
    if (!interaction.isModalSubmit()) return;

    if (interaction.customId === 'modal_acao') {
        const actionName = interaction.fields.getTextInputValue('nome_acao');
        const vagas = parseInt(interaction.fields.getTextInputValue('vagas_acao'));

        if(isNaN(vagas)) {
            return interaction.reply({ content: 'N√∫mero de vagas inv√°lido!', ephemeral: true });
        }

        // Usando Map para armazenar dados tempor√°rios
        tempActions.set(interaction.user.id, {
            name: actionName,
            vagas: vagas
        });

        const armaSelect = new ActionRowBuilder()
            .addComponents(
                new SelectMenuBuilder()
                    .setCustomId('arma_select')  // Simplificando o customId
                    .setPlaceholder('Foi pega arma do ba√∫?')
                    .addOptions([
                        { label: 'Sim', value: 'sim', description: 'Armas do ba√∫ foram pegas' },
                        { label: 'N√£o', value: 'nao', description: 'Nenhuma arma do ba√∫ foi pega' }
                    ])
            );

        await interaction.reply({
            content: 'Foi pega arma do ba√∫?',
            components: [armaSelect],
            ephemeral: true
        });
    }
});

// Tratando a sele√ß√£o de armas
client.on('interactionCreate', async (interaction) => {
    if (!interaction.isSelectMenu()) return;
    
    if (interaction.customId === 'arma_select') {
        const tempData = tempActions.get(interaction.user.id);
        if (!tempData) {
            return interaction.reply({ 
                content: 'Erro: Sess√£o expirada. Por favor, crie a a√ß√£o novamente.', 
                ephemeral: true 
            });
        }

        const pegouArma = interaction.values[0] === 'sim';

        if (pegouArma) {
            const quantidadeModal = new ModalBuilder()
                .setCustomId('quantidade_armas_modal')
                .setTitle('Quantidade de Armas');

            const quantidadeInput = new TextInputBuilder()
                .setCustomId('quantidade_armas')
                .setLabel('Quantas armas foram pegas do ba√∫?')
                .setStyle(TextInputStyle.Short)
                .setPlaceholder('Digite o n√∫mero de armas')
                .setRequired(true);

            quantidadeModal.addComponents(
                new ActionRowBuilder().addComponents(quantidadeInput)
            );

            tempData.pegouArma = true;
            await interaction.showModal(quantidadeModal);
        } else {
            // Criar a√ß√£o sem armas
            const actionId = Date.now();
            actions[actionId] = {
                name: tempData.name,
                vagas: tempData.vagas,
                pegouArma: false,
                quantidadeArmas: 0,
                participantes: [],
                reservas: []
            };

            tempActions.delete(interaction.user.id); // Limpa dados tempor√°rios

            const buttons = new ActionRowBuilder()
                .addComponents(
                    new ButtonBuilder()
                        .setCustomId(`Participar_${actionId}`)
                        .setLabel('‚úÖ Participar')
                        .setStyle(ButtonStyle.Success),
                    new ButtonBuilder()
                        .setCustomId(`Cancelar_${actionId}`)
                        .setLabel('‚ùå Cancelar')
                        .setStyle(ButtonStyle.Danger)
                );

            await interaction.update({ components: [], content: 'A√ß√£o criada com sucesso!', ephemeral: true });
            await interaction.channel.send({
                embeds: [{
                    color: 0x0099FF,
                    title: `üéÆ ${tempData.name}`,
                    description: `
üìÖ **Data:** <t:${Math.floor(actionId / 1000)}:F>

üë• **Vagas:** 0/${tempData.vagas}
üó°Ô∏è **Arma do ba√∫:** N√£o

**Participantes:**
*Nenhum participante ainda*`,
                    footer: {
                        text: 'Use os bot√µes abaixo para participar ou se retirar da a√ß√£o!'
                    }
                }],
                components: [buttons]
            });
        }
    }
});

